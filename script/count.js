document.getElementById('comBtn-1').addEventListener('click', function(){
    const numPlus = document.getElementById('number-plus');
    const numMinus = document.getElementById('number-minus');
    let currentNumPlus = parseInt(numPlus.textContent);
    let currentNumMinus = parseInt(numMinus.textContent);

    numPlus.textContent = currentNumPlus + 1;
    numMinus. textContent = currentNumMinus - 1;

})
 
